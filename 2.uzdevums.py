# Izveidojiet Python programmu, kas izmanto while ciklu, lai atrastu pirmo skaitli, kura kvadrāts nav lielāks par 1000.

# Sākumskaitlis
skaitlis = 1

# Izmantoju While ciklu, kurš turpinās, kamēr kvadrāts ir lielāks par 1000
while skaitlis**2 <= 1000:
    skaitlis += 1

# Izvadē tiek parādīts/uzrādīts rezultāts
print(f"Pirmāis skaitlis, kura kvadrāts nav lielāks par 1000, ir: {skaitlis}")
